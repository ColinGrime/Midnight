package me.colingrimes.midnight.event.util;

/**
 * An enumeration representing actions related to armor equipping.
 */
public enum EquipAction {
    /**
     * The action of equipping armor.
     */
    EQUIP,

    /**
     * The action of unequipping armor.
     */
    UNEQUIP,

    /**
     * The action of swapping armor.
     */
    SWAP
}
